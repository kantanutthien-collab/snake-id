#!/usr/bin/env node
// Filter raw iNat dump → SnakeCandidate[] merged with existing curation.
// Usage: node scripts/build-snake-data.mjs
//
// In:  src/data/thai-snakes-raw.json
//      src/data/snakes.ts  (existing curated 6)
// Out: src/data/snake-candidates.ts

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const RAW = resolve(ROOT, "src/data/thai-snakes-raw.json");
const SNAKES_TS = resolve(ROOT, "src/data/snakes.ts");
const OUT = resolve(ROOT, "src/data/snake-candidates.ts");

const MIN_OBSERVATIONS = 20;

// Marine / sea-snake genera — terrestrial field guide skips these.
const MARINE_GENERA = new Set([
  "Hydrophis",
  "Laticauda",
  "Aipysurus",
  "Emydocephalus",
  "Microcephalophis",
  "Pelamis",
  "Hydrelaps",
  "Kerilia",
  "Kolpophis",
  "Lapemis",
  "Thalassophis",
]);

function slugify(sci) {
  return sci.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function fixWikipediaUrl(url) {
  if (!url) return null;
  // iNat returns URLs with spaces; canonicalize to https + underscores.
  return url.replace(/^http:/, "https:").replace(/ /g, "_");
}

// Parse existing curated snakes.ts by extracting object literals.
// Crude but avoids needing a TS parser in the script.
function parseExistingCuration() {
  const src = readFileSync(SNAKES_TS, "utf8");
  const entries = new Map();
  // Match each object literal starting at "{" after a "[" or ",".
  const re = /\{\s*id:\s*"([^"]+)"[\s\S]*?firstAid:\s*"[\s\S]*?",?\s*\}/g;
  let m;
  while ((m = re.exec(src))) {
    const block = m[0];
    const get = (key, isArray = false) => {
      if (isArray) {
        const a = block.match(new RegExp(`${key}:\\s*\\[([\\s\\S]*?)\\]`));
        if (!a) return null;
        return [...a[1].matchAll(/"((?:[^"\\]|\\.)*)"/g)].map((mm) =>
          mm[1].replace(/\\"/g, '"')
        );
      }
      const s = block.match(new RegExp(`${key}:\\s*"((?:[^"\\\\]|\\\\.)*)"`));
      if (s) return s[1].replace(/\\"/g, '"');
      const n = block.match(new RegExp(`${key}:\\s*(\\d+)`));
      if (n) return Number(n[1]);
      return null;
    };
    const sci = get("sci");
    if (!sci) continue;
    entries.set(sci, {
      id: get("id"),
      en: get("en"),
      th: get("th"),
      family: get("family"),
      danger: get("danger"),
      venom: get("venom"),
      size: get("size"),
      swatch: get("swatch", true),
      idCues: get("idCues", true),
      habitat: get("habitat"),
      handle: get("handle"),
      firstAid: get("firstAid"),
    });
  }
  return entries;
}

function buildCandidate(raw, curated) {
  const base = {
    id: curated?.id || slugify(raw.sci),
    sci: raw.sci,
    en: raw.en,
    th: curated?.th ?? null,
    family: raw.family,
    genus: raw.genus,
    inatId: raw.inat_id,
    observationsThailand: raw.observations_thailand,
    wikipediaUrl: fixWikipediaUrl(raw.wikipedia_url),
    wikipediaSummary: raw.wikipedia_summary,
    photo: raw.photo,
    draft: !curated,
  };
  if (curated) {
    if (curated.danger != null) base.danger = curated.danger;
    if (curated.venom) base.venom = curated.venom;
    if (curated.size) base.size = curated.size;
    if (curated.swatch && curated.swatch.length === 3) base.swatch = curated.swatch;
    if (curated.idCues && curated.idCues.length) base.idCues = curated.idCues;
    if (curated.habitat) base.habitat = curated.habitat;
    if (curated.handle) base.handle = curated.handle;
    if (curated.firstAid) base.firstAid = curated.firstAid;
  }
  return base;
}

function serializeTs(candidates) {
  const json = JSON.stringify(candidates, null, 2);
  return (
    '// Generated by scripts/build-snake-data.mjs — do not edit by hand.\n' +
    '// Re-run the script to regenerate after updating raw iNat data or curation.\n\n' +
    'import type { SnakeCandidate } from "@/lib/types";\n\n' +
    `export const snakeCandidates: SnakeCandidate[] = ${json};\n`
  );
}

function main() {
  const raw = JSON.parse(readFileSync(RAW, "utf8"));
  const curated = parseExistingCuration();

  const filtered = raw.filter((r) => {
    if (r.observations_thailand < MIN_OBSERVATIONS) return false;
    if (r.genus && MARINE_GENERA.has(r.genus)) return false;
    return true;
  });

  filtered.sort((a, b) => b.observations_thailand - a.observations_thailand);

  const candidates = filtered.map((r) => buildCandidate(r, curated.get(r.sci)));
  writeFileSync(OUT, serializeTs(candidates));

  const matched = candidates.filter((c) => !c.draft).length;
  const withPhoto = candidates.filter((c) => c.photo).length;
  process.stderr.write(
    `Raw: ${raw.length} → filtered: ${filtered.length} (dropped < ${MIN_OBSERVATIONS} obs + marine genera)\n`
  );
  process.stderr.write(`  matched existing curation: ${matched}\n`);
  process.stderr.write(`  draft (needs curation):    ${candidates.length - matched}\n`);
  process.stderr.write(`  with CC photo:             ${withPhoto}\n`);
  process.stderr.write(`Wrote ${OUT}\n`);

  // Report any existing curation that didn't survive the filter.
  for (const sci of curated.keys()) {
    if (!candidates.some((c) => c.sci === sci)) {
      process.stderr.write(`  WARNING: existing curation for ${sci} not in filtered set\n`);
    }
  }
}

main();
